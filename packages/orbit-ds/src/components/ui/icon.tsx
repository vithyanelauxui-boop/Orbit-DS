export const iconNames = Object.keys(iconMap) as Array<keyof typeof iconMap>

export interface IconComponentProps extends IconProps {
  name: keyof typeof iconMap
}

export const Icon = React.forwardRef<SVGSVGElement, IconComponentProps>(
  ({ name, ...props }, ref) => {
    const iconName = iconMap[name]

    if (!iconName) {
      console.warn(`Icon "${name}" not found`)
      return null
    }

    const IconComponent = Icons[iconName] as React.ComponentType<IconProps>

    return <IconComponent ref={ref} {...props} />
  }
)

Icon.displayName = "Icon"
